# Linear vs Non-Linear Data structures.
# Linear data structures: Elements are ordered in sequential manner or linear manner ; adjacently; single level; single traversal
# Non-linear data structures: ELements are ordered in hierarcy; multiple number of traversals
# Linear: array, linked_list, stack, queue, hash_table
# Non-linear: tree, graph